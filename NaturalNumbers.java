public class NaturalNumbers{

public static void main(String[] args){

int i, num= 100, sum= 0;		// initializing variables

for( i = 1; i<=num; ++i)		// run loop until the condition return true
{
sum = sum+ i;			// adding value to i to sum variable
 }
System.out.println("sum of 100 natural numbers is = " + sum); // print value of sum
}
}
